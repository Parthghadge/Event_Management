    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/seProject?autoRecconct=true&useSSL=false", "root", "mysql123");
            Statement st = con.createStatement();
            ResultSet rs= st.executeQuery("Select vmid,password from venue_mgrdb");
            String id=txtUsername.getText();
            String password=txtPassword.getText();
            while(rs.next())
            {
                if(id.equals(rs.getString(1))&&password.equals(rs.getString(2)))
                {
                    JOptionPane.showMessageDialog(this,"Login Successful!","Success",JOptionPane.OK_OPTION);
                    new VMmenu(id).setVisible(true);
                    dispose();
                    return;
                }
            }

            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(this,"Incorrect username or password","Login Failed",JOptionPane.ERROR_MESSAGE);
                txtUsername.setText("");
                txtPassword.setText("");
            }
        }
        catch (Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_loginBtnActionPerformed

